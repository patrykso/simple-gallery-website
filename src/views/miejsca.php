            <p><br>
                Cinque Terre – fragment riwiery liguryjskiej położonej pomiędzy cyplem Mesco, w okolicy Levanto, a przylądkiem Montenero przy Portovenere, na którym znajduje się pięć miejscowości: Monterosso, Vernazza, Corniglia, Manarola i Riomaggiore.
                <br><br>
                Cinque Terre razem z Portovenere i wyspami Palmaria, Tino i Tinetto w 1997 roku zostały wpisane na listę światowego dziedzictwa kulturalnego i przyrodniczego UNESCO jako Portovenere, Cinque Terre i wyspy (Palmaria, Tino i Tinetto). Wpisu dokonano, gdyż odcinek wybrzeża, przy którym znajdują się wymienione miejscowości i wyspy, stanowi krajobraz kulturowy o znaczących walorach panoramicznych i kulturalnych, a plany i lokalizacja miejscowości w otaczającym je krajobrazie wyznaczają etapy ciągłości osadniczej w tym regionie na przestrzeni ostatniego tysiąclecia.
                <br><br>
                W celu swobodnego poruszania się po Parku Narodowym Cinque Terre należy wykupić specjalną Cinque Terre Card lub Cinque Terre Card Treno. Karta upoważnia do wejścia na ścieżki Cinque Terre, w tym na Drogę Miłości. Dodatkowo karta Cinque Terre Card Treno upoważnia do nielimitowanej liczby przejazdów pociągami pomiędzy miasteczkami Parku w ciągu jednego dnia (około 30 połączeń dziennie w każdym z kierunków). Trasa kolejowa wiedzie w tunelach i na skalistych klifach wzdłuż morza.
